# Numbers --------------------------------------------------------------------------

# Int
a = 33 

# Float
b = 12.5

# Complex
c = 3 + 4j

print(type(a), a)
print(type(b), b)
print(type(c), c)


# Strings --------------------------------------------------------------------------
username = 'Manish'
print(type(username), username)

# string treated as array of characters
print(username[1])

# negative counting starts from the end
print(username[-1])

# print in between characters - below code will print form index - 1 to 3 not 4
print(username[1:4]) 


